package com.technoelevate.grocery.add.cart;

public class GroceryItems {
	String nonVegItems;
	String frozons;
	String nonFoodItems;
	String vegetables;

	public GroceryItems(String nonVegItems, String frozons, String nonFoodItems, String vegetables) {

		this.nonVegItems = nonVegItems;
		this.frozons = frozons;
		this.nonFoodItems = nonFoodItems;
		this.vegetables = vegetables;
	}

}
