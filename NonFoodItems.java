package com.technoelevate.grocery.add.cart;

public class NonFoodItems {
	public String soapbars;
	public String toothpaste;
	public String liquids;
	public String hairOil;
	public String washableItems;
	public NonFoodItems(String soapbars, String toothpaste, String liquids, String hairOil, String washableItems) {
		super();
		this.soapbars = soapbars;
		this.toothpaste = toothpaste;
		this.liquids = liquids;
		this.hairOil = hairOil;
		this.washableItems = washableItems;
	}

}
